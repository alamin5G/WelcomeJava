package Listing_2;

public class ExponentialOperation {
    public static void main(String[] args){
        System.out.println("2^3 = " + Math.pow(2, 3));
        System.out.println("4^0.5 = " + Math.pow(4, 0.5));
        System.out.println("2^2.5 = " + Math.pow(2, 2.5));
        System.out.println("2^-2 = " + Math.pow(2, -2));
    }
}
