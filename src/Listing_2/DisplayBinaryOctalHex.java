package Listing_2;

public class DisplayBinaryOctalHex {
    public static void main(String[] args) {
        System.out.println(0B111); //OUTPUT - 7 -- 0B TO INDICATE BINARY
        System.out.println(07); //OUTPUT - 7 -- 0 TO INDICATE OCTAL
        System.out.println(0xF); //OUTPUT - 15; -- 0X TO INDICATE HEXADECIMAL
    }
}
